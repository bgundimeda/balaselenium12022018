package dec3;

public class Variables {

	public static void main(String[] args) {
		
		//PRIMITIVE & NON PRIMITIVE DATA TYPES
	
		int firstnumber;
		
		firstnumber = 7700;
		
		firstnumber = ++firstnumber;
		
		System.out.println("First Number : "+firstnumber);
		
		byte SecondNumber = 100;
		
		long ThirdNumber = 4565474746464l;
		
		boolean flag;
		
		flag = false;
		
		char CharVar = '1';
		
		double MyDoubleVar = 10.7;
		
		float MyFloatVar = 1324.47455545f;
		
		String str = "Welcome to my world";
		
		System.out.println(str);
		
		System.out.println("Second Number : "+SecondNumber);
		
		System.out.println("Third Number : "+ThirdNumber);
		
		System.out.println("Character Value : "+CharVar);
		
		System.out.println("Boolean Value : " +flag);
		
		System.out.println("Double Number : "+MyDoubleVar);
		
		System.out.println("Float Number : "+MyFloatVar);

	}

}
